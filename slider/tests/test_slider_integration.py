class TestFullWorkflow:
    async def test_create_add_content_verify_binary(self, live_context):
        # Create
        create_result = await slide_maker.execute_action(
            "create_presentation",
            {"title": "Q4 Report", "subtitle": "Integration Test"},
            live_context,
        )
        assert create_result.type == ResultType.ACTION
        presentation_id = create_result.result.data["presentation_id"]
        file_obj = create_result.result.data["file"]

        # Add a slide
        add_slide_result = await slide_maker.execute_action(
            "add_slide",
            {"presentation_id": presentation_id, "files": [file_obj]},
            live_context,
        )
        assert add_slide_result.type == ResultType.ACTION
        assert add_slide_result.result.data["slide_count"] == 2
        file_obj = add_slide_result.result.data["file"]

        # Add elements to slide 1
        add_result = await slide_maker.execute_action(
            "add_elements",
            {
                "presentation_id": presentation_id,
                "slide_index": 1,
                "elements": [{"content": "**Key Metrics**"}, {"content": "- Revenue: $1M\n- Users: 10k"}],
                "files": [file_obj],
            },
            live_context,
        )
        assert add_result.type == ResultType.ACTION
        file_obj = add_result.result.data["file"]

        assert file_obj["name"].endswith(".pptx")
        assert_valid_pptx(file_obj)
